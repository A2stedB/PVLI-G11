export const Orientation = Object.freeze({
    N:(0),
    E:(90),
    S:(180),
    W:(270),

    /**
     * 
     * @param {Orientation} direction 
     * @returns {Array{Orientation}}
     */
    getAvailableDirection(direction){
        return [direction,(direction+90)%360,(direction-90)%360]
    }
})